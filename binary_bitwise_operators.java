public class binary_bitwise_operators {
    public static void main(String args[]){
        int a=5,b=6;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b); 
        System.out.println(~(-5));
        System.out.println(~5);
        System.out.println(5<<2);
        System.out.println(5>>2);

    }    
}

public class primitivedatatypes {
    public static void main(String args[]){
        int n=4253;
        float k=7.1f; /* If you write float k=7.1 then it will interpreted as double. 
        Double is a 64 bit precision IEEE 754 floating point, while float is a 32 bit precision IEEE 754 floating point.
        So to take 7.1 as float you should 7.1f */
        long l=33423975;
        short sh=234;
        double d=3.54521;
        byte by=8;
        boolean bm=true;
        char ch='m';
        System.out.println(n);
        System.out.println(k);
        System.out.println(l);
        System.out.println(sh);
        System.out.println(d);
        System.out.println(by);
        System.out.println(bm);
        System.out.println(ch);
    }
    
}

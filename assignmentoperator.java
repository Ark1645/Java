public class assignmentoperator {
    public static void main(String args[]){
        int a=10,b=6;
        System.out.println(a+" "+b);
        a=b;
        System.out.println(a+" "+b);
        a+=10;
        b*=4;
        System.out.println(a+" "+b);
        a-=4;
        b/=3;
        System.out.println(a+" "+b);
        int c=8;
        c%=4;
        System.out.println(c); 
    }
}
